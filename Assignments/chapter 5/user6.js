
// Celcius to Farenheit Conversion
var celciusTemp = 40;
var farenheit = (celciusTemp * 9/5) + 32;
document.write("40<sup>o</sup>C  is " + farenheit + "<sup>o</sup>F </br>");

// Farenheit to  Celcius Conversion
var farenheitTemp = 60;
var celcius = (farenheitTemp - 32) * 5/9;
document.write("60<sup>o</sup>F  is " + celcius + "<sup>o</sup>C");
